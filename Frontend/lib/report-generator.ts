function renderHeader(title: string, subtitle?: string): string {
  const now = new Date().toLocaleDateString("en-GB", { day: "2-digit", month: "short", year: "numeric" });
  return `<div class="header">
    <div><h1>${title}</h1><div class="sub">${subtitle || ""}</div></div>
    <div class="brand">${BRAND.name}<br>${BRAND.subtitle}<br>Generated: ${now}</div>
  </div>`;
}

function renderFooter(): string {
  return `<div class="footer">This document was generated by ${BRAND.name} ${BRAND.subtitle}. Confidential — for internal use only.</div>`;
}

function openPrintWindow(html: string) {
  const w = window.open("", "_blank");
  if (w) {
    w.document.write(`<!DOCTYPE html><html><head><style>${baseStyles()}</style></head><body>${html}<script>window.onload=function(){window.print()}<\/script></body></html>`);
    w.document.close();
  }
}

/**
 * EXPORT FUNCTIONS (XLSX/CSV)
 */
export function exportToXLSX(data: Record<string, any>[], fileName: string, sheetName = "Report") {
  const ws = XLSX.utils.json_to_sheet(data);
  const wb = XLSX.utils.book_new();
  XLSX.utils.book_append_sheet(wb, ws, sheetName);
  XLSX.writeFile(wb, `${fileName}.xlsx`);
}

