export function pdfWorkerMaster(workers: any[]) {
  const rows = workers.map(w => `<tr>
    <td class="mono">${w.emp_id || "—"}</td><td>${w.name_en || "—"}</td><td>${w.iqama_no || "—"}</td>
    <td>${w.nationality || "—"}</td><td>${w.designation || w.occupation_en || "—"}</td>
    <td>${w.clients?.legal_name || "Unassigned"}</td><td>${w.work_status || "Active"}</td>
    <td class="r mono">${sar(w.basic_salary)}</td><td>${w.mobile || "—"}</td>
  </tr>`).join("");
  openPrintWindow(`${renderHeader("Worker Master Report", `Total: ${workers.length} workers`)}
    <table><thead><tr><th>EMP ID</th><th>Name</th><th>Iqama</th><th>Nationality</th><th>Designation</th><th>Client</th><th>Status</th><th class="r">Basic (SAR)</th><th>Mobile</th></tr></thead><tbody>${rows}</tbody></table>${renderFooter()}`);
}

export function pdfDocumentExpiry(workers: any[]) {
  const now = new Date();
  const items = workers.map(w => {
    const docs: any[] = [];
    if (w.iqama_expiry) docs.push({ type: "Iqama", no: w.iqama_no, expiry: w.iqama_expiry });
    if (w.passport_expiry_date) docs.push({ type: "Passport", no: w.passport_no, expiry: w.passport_expiry_date });
    if (w.health_insurance_expiry) docs.push({ type: "Health Insurance", no: w.health_insurance_no, expiry: w.health_insurance_expiry });
    return docs.map(d => ({ ...d, name: w.name_en, emp_id: w.emp_id, days: Math.ceil((new Date(d.expiry).getTime() - now.getTime()) / 86400000) }));
  }).flat().filter(d => d.days <= 90).sort((a, b) => a.days - b.days);

  const rows = items.map(d => {
    const cls = d.days <= 0 ? "badge-red" : d.days <= 30 ? "badge-yellow" : "badge-green";
    const label = d.days <= 0 ? "EXPIRED" : `${d.days}d left`;
    return `<tr><td>${d.name}</td><td class="mono">${d.emp_id || "—"}</td><td>${d.type}</td><td class="mono">${d.no || "—"}</td><td>${d.expiry}</td><td class="c"><span class="badge ${cls}">${label}</span></td></tr>`;
  }).join("");
  openPrintWindow(`${renderHeader("Document Expiry Report", `${items.length} documents expiring within 90 days`)}
    <table><thead><tr><th>Worker</th><th>EMP ID</th><th>Document</th><th>Number</th><th>Expiry</th><th class="c">Status</th></tr></thead><tbody>${rows}</tbody></table>${renderFooter()}`);
}

